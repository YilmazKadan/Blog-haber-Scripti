// Bu fonksiyon kendim yazmış olduğum tarih farklarını saat dakika ay yıl vs cinsinde yazdırır.
function tarihfarki($tarih1){
    $tarih1=new datetime($tarih1);
    date_default_timezone_set('Europe/Istanbul');
    $bugununtarihi=new datetime(date('d.m.Y H:i:s'));//İsatanbulun şu anki tarihini alıyoruz ve datetime formatına çeviriyoruz.Çevirmezsek farkı bulamayaız
    $deger=$tarih1->diff($bugununtarihi);//Fonksiyonda parametre olarak aldığımız tarih bilgisini diff fonksiyonu ile aralarındaki farkı deger değişkenine atyoruz.
    $dakika=$deger->format('%i');//Saat dilimlerini verir
    $saat=$deger->format('%h');
    $gun=$deger->format('%d');
    $ay=$deger->format('%m');
    $yıl=$deger->format('%y');
    if ($yıl<1) {//Burda ise değerler 1 den küçük ise bir alt tarihe bakıyoruz eğer büyük ise direk olarak return edip ekrana bastırıyoruz.Örneğin yıl 1 den küçükse aya bakıyor oda küçükse güne oda küçük ise saate sonra dakikaya en son ise saniyeye bakıyor eğer büyük ise yazdırıyoruz.
        if ($ay<1) {
            if ($gun<1) {
                if ($saat<1) {
                    if ($dakika<1) {
                        return $deger->format('%s Saniye önce');
                    }
                    else{
                        return $deger->format('%i Dakika  önce');
                    }
                }
                else{
                    return $deger->format('%h Saat  önce');
                }
            }
            else{
                if ($gun>=7){
                    
                    return floor($gun/7).' Hafta '.($gun%7).' gün önce';
                }
                else{
                    return $deger->format('%d Gün %h saat önce');
                }

            }
        }
        else{
            return $deger->format('%m Ay önce');
        }
    }
    else{
        return $deger->format('%y Yıl %m önce');

    }
}
