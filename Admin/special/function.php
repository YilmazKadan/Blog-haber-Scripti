function turkce_cevir($metin) {
	$metin = trim($metin);
	$trharf = array('Ç','ç','Äž','ğ','ı','İ','Ö','ö','Ş','ş','Ü','ü',' ',';',',');
	$enharf = array('C','c','G','g','i','I','O','o','S','s','U','u','-','-','-');
	$turkce = str_replace($trharf,$enharf,$metin);
	return $turkce;
}  
